    public void store(String column)
    {
       try
        {
            in.clear();
            String sql="SELECT * FROM Item_List";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next())
                in.add(rs.getString(column));
        }
        catch(Exception e){JOptionPane.showMessageDialog(null, e);}
    }
    public void autofill(String text,javax.swing.JTextField txt){
        int start = text.length();
        int last = start;
        
        for(int i=0;i<in.size();i++)
        {
            String complete=in.get(i);
            
            if(complete.toUpperCase().startsWith(text.toUpperCase()))
            {
                txt.setText(complete);
                last=complete.length();
                break;
            }
        }
        if(start<last)
        {
            txt.setCaretPosition(last);
            txt.moveCaretPosition(start);
        }
    }
