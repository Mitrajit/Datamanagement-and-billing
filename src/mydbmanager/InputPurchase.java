    private void jTextField5KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyPressed
        // TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_BACK_SPACE:
                break;
            case KeyEvent.VK_ENTER:
                jTextField5.setText(jTextField5.getText());
                break;
            default:
                if((evt.getKeyCode()>=48&&evt.getKeyCode()<=57)||(evt.getKeyCode()>='A'&&evt.getKeyCode()<='Z')||(evt.getKeyCode()>='a'&&evt.getKeyCode()<='z'))
                EventQueue.invokeLater(new Runnable() {
            @Override
            public void run() {
                if(jTextField5.getText().length()==1)
                store("Transportation");
                 autofill(jTextField5.getText(),jTextField5);
            }
        });
            
        }
    }//GEN-LAST:event_jTextField5KeyPressed

